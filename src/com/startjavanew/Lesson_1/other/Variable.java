package com.startjavanew.Lesson_1.other;

public class Variable {
    public static void main(String[] args) {
        byte memorySize = 4;
        short numberOfDisks = 2;
        int digitCapacity = 64;
        long year = 2020;
        float processorGhz = 1.9F;
        double freeMemory = 3.87D;
        char winVersion = '7';
        boolean isNotebook = true;

        System.out.println("memorySize - " + memorySize);
        System.out.println("numberOfDisks - " + numberOfDisks);
        System.out.println("digitCapacity - " + digitCapacity);
        System.out.println("year - " + year);
        System.out.println("processorGhz - " + processorGhz);
        System.out.println("freeMemory - " + freeMemory);
        System.out.println("winVersion - " + winVersion);
        System.out.println("isNotebook - " + isNotebook);
    }        
} 